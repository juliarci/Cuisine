
public class valmenu {
private Long id;
private int qty;

public valmenu(Long id, int qty) {
	super();
	this.id = id;
	this.qty = qty;
}

public Long getId() {
	return id;
}

public void setId(Long id) {
	this.id = id;
}

public int getQty() {
	return qty;
}

public void setQty(int qty) {
	this.qty = qty;
}

}

